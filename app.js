function renderMeta(meeting, report) {
  const items = [];
  const date = formatDate(meeting.created_at);
  const duration = durationLabel(report.stats?.duration_seconds || meeting.duration_seconds);

  if (date) items.push(`<span class="meta-item">📅 ${escapeHtml(date)}</span>`);
  if (duration) items.push(`<span class="meta-item">🕘 ${escapeHtml(duration)}</span>`);
  items.push(`<span class="meta-item">— ${escapeHtml(t('generatedBy'))}</span>`);

  $('meetingMeta').innerHTML = items.join('');
}

function renderStats(report) {
  const stats = [];
  const duration = durationLabel(report.stats?.duration_seconds);
  const participantsCount = report.participants?.length || 0;
  const tasksCount = report.tasks.length;
  const decisionsCount = report.decisions.length;
  const risksCount = report.risks.length;

  if (duration) stats.push({ icon: '⏱', value: duration, label: t('duration') });
  if (participantsCount > 0) stats.push({ icon: '👥', value: participantsCount, label: t('participants') });
  if (tasksCount > 0) stats.push({ icon: '☑', value: tasksCount, label: t('actionItems') });
  if (decisionsCount > 0) stats.push({ icon: '⚖', value: decisionsCount, label: t('decisionCount') });
  if (risksCount > 0) stats.push({ icon: '⚠', value: risksCount, label: t('riskCount') });

  if (!stats.length) return;

  $('statsSection').innerHTML = stats.map((s) => `
    <div class="stat-item">
      <div class="stat-icon">${escapeHtml(s.icon)}</div>
      <div><div class="stat-value">${escapeHtml(s.value)}</div><div class="stat-label">${escapeHtml(s.label)}</div></div>
    </div>
  `).join('');
  $('statsSection').classList.remove('hidden');
}

function renderMetrics(report) {

  const metrics = report.key_metrics || report.metrics || [];
  if (!Array.isArray(metrics) || !metrics.length) return;
  const grid = $('metricsContent');
  let cols = metrics.length;
  if (metrics.length >= 7) {
    cols = 4;
  }

  grid.style.setProperty('--metric-cols', cols);
  grid.innerHTML = metrics.map(metric => `
    <div class="metric-card">
      <div class="metric-label">${escapeHtml(metric.label || '')}</div>
      <div class="metric-value">${escapeHtml(metric.value || '')}</div>
    </div>
  `).join('');
  $('metricsSection').classList.remove('hidden');
}

function renderDynamicCards(report) {
  const blocks = [];
  
  if (report.insights?.length) {
    blocks.push({
      key: 'insights',
      title: t('insights'),
      items: report.insights
    });
  }

  if (report.decisions?.length) {
    blocks.push({
      key: 'decisions',
      title: t('decisions'),
      items: report.decisions
    });
  }

  if (report.risks?.length) {
    blocks.push({
      key: 'risks',
      title: t('risks'),
      items: report.risks
    });
  }

  if (!blocks.length) return;
  const grid = $('insightsDecisionsRisksGrid');
  grid.style.setProperty('--grid-cols', 1);
  const sectionsHtml = blocks.map(block => {

    const html = block.items.map(item => {
      // Старый формат (строка)
      if (typeof item === 'string') {
        return `
          <div class="report-item">
            <span class="report-dot"></span>
            <div>${escapeHtml(item)}</div>
          </div>
        `;
      }
      // Новый формат (объект)
      return `
        <div class="report-item">
          <span class="report-dot"></span>
          <div>
            <strong>${escapeHtml(item.title || '')}</strong>
            ${
              item.details
                ? `<div class="item-description">${escapeHtml(item.details)}</div>`
                : ''
            }
          </div>
        </div>
      `;
    }).join('');

    return `
    <div class="highlight-section ${block.key}">
   <h3>${escapeHtml(block.title)}</h3>
    <div class="item-list">
        ${html}
    </div>
</div>
    `;
  }).join('');
  grid.innerHTML = `
<section class="dynamic-card highlights">
    ${sectionsHtml}
</section>
`;
grid.classList.remove('hidden');
}

