function escapeHtml(value) {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function isEmptyValue(value) {
  if (value == null) return true;
  if (Array.isArray(value)) return value.length === 0;
  if (typeof value === 'object') return Object.keys(value).length === 0;
  const normalized = String(value).replace(/[#*•\-\s.]/g, '').trim().toLowerCase();
  return !normalized || ['неуказано', 'not specified', 'n/a', 'none', 'null'].includes(normalized);
}

function normalizeReport(report = {}) {
  return {
       headline: report.headline || '',

    title: report.title || report.meeting_title || '',
    subtitle: report.subtitle || report.meeting_subtitle || '',
    objective: report.objective || report.meeting_objective || '',
    meeting_type: report.meeting_type || '',
    importance: report.importance || report.meeting_importance || '',
    importance_reason: report.importance_reason || '',

    summary: report.executive_brief || report.summary || '',
    architecture: report.architecture || { sections: [] },
    insights: toList(report.key_takeaways || report.insights),
    decisions: toList(report.decisions),
    key_metrics: toList(report.key_metrics || report.metrics),
    metrics: toList(report.metrics),
    risks: toList(report.risks),
    dependencies: toList(report.dependencies),
    tasks: toList(report.tasks),
    owners: toList(report.owners || report.responsibles),
    participants: Array.isArray(report.participants) ? report.participants : [],
    stats: report.stats || {}
  };
}

function toList(value) {
  if (Array.isArray(value)) {
    return value.filter((v) => !isEmptyValue(v));
  }

  if (isEmptyValue(value)) return [];
  return String(value)
    .split(/\n+/)
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => line.replace(/^[-•]\s*/, '').trim())
    .filter((line) => !isEmptyValue(line));
 }

function parseTask(line) {
  const parts = String(line).split(/\s+—\s+|\s+-\s+/).map((p) => p.trim()).filter(Boolean);
  return {
    task: parts[0] || line,
    owner: parts[1] || '',
    dueDate: parts.slice(2).join(' — ') || ''
  };
}

function parseOwner(owner) {
    if (owner && typeof owner === 'object') {
        return {
            name: owner.name || owner.owner || owner.person || '',
            role: owner.role || owner.title || ''
        };
    }
    const parts = String(owner)
        .split(/\s+–\s+|\s+-\s+/)
        .map(p => p.trim())
        .filter(Boolean);
    return {
        name: parts[0] || owner,
        role: parts.slice(1).join(' – ')
    };
}

function initials(name) {
  return String(name || '')
    .split(/\s+/)
    .filter(Boolean)
    .slice(0, 2)
    .map((part) => part[0])
    .join('')
    .toUpperCase() || 'M';
}

function formatDate(value) {
  if (!value) return '';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '';
  const locale = currentLang === 'ru' ? 'ru-RU' : currentLang === 'es' ? 'es-ES' : currentLang === 'pt' ? 'pt-PT' : 'en-US';
  return new Intl.DateTimeFormat(locale, { day: 'numeric', month: 'long', year: 'numeric' }).format(date);
}

function durationLabel(seconds) {
  const s = Number(seconds);
  if (!s || s <= 0) return '';
  const minutes = Math.round(s / 60);
  return `${minutes} ${currentLang === 'ru' ? 'мин' : 'min'}`;
}

function renderTextBlock(text) {
  if (isEmptyValue(text)) return '';

  const lines = String(text).split('\n').map((l) => l.trim()).filter(Boolean);
  if (!lines.length) return '';

  const html = [];
  let listOpen = false;

  for (const line of lines) {
    const bullet = line.match(/^[-•]\s+(.*)$/);
    if (bullet) {
      if (!listOpen) {
        html.push('<ul>');
        listOpen = true;
      }
      html.push(`<li>${escapeHtml(bullet[1])}</li>`);
    } else {
      if (listOpen) {
        html.push('</ul>');
        listOpen = false;
      }
      html.push(`<p>${escapeHtml(line)}</p>`);
    }
}
  
  if (listOpen) html.push('</ul>');
  return html.join('');
}

function renderArchitecture(architecture) {

    if (!architecture) return '';
    const sections = architecture.sections || [];
    if (!sections.length) return '';
    return sections.map(section => {
        const items = section.items || [];

        if (!items.length) return '';

        return `
            <div class="architecture-section">
                <h3 class="architecture-section-title">
                    ${escapeHtml(section.title || '')}
                </h3>

                <div class="architecture-${section.layout}">

                    ${items.map(item => `
                        <div class="architecture-item">
                            <h4>${escapeHtml(item.title || '')}</h4>
                            <p>${escapeHtml(item.description || '')}</p>
                        </div>
                    `).join('')}
                </div>
            </div>
        `;
    }).join('');
}

